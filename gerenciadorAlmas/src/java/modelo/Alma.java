package modelo;

public class Alma {

    private Integer id;
    private String nome;
    private String pecado;
    private Integer status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPecado() {
        return pecado;
    }

    public void setPecado(String pecado) {
        this.pecado = pecado;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }
    
}
